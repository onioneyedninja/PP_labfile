print(f"Data Type of string_variable: {type(string_variable)}")
print(f"Data Type of list_variable: {type(list_variable)}")
print(f"Data Type of tuple_variable: {type(tuple_variable)}")
print(f"Data Type of dictionary_variable: {type(dictionary_variable)}")
